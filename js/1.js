//JUGADOR 1
let totalJugador1 = document.getElementById('TJ1');
let sumattJ1 = document.getElementById('sumarTotalJug1');

function sumarJ1 (){
    sumattJ1.style="display: none;";
    let ttJ1 = document.createElement('p');
    let unoJ1 = document.getElementById('1Jugador1').value;
    let dosJ1 = document.getElementById('2Jugador1').value;
    let tresJ1 = document.getElementById('3Jugador1').value;
    let cuatroJ1 = document.getElementById('4Jugador1').value;
    let cincoJ1 = document.getElementById('5Jugador1').value;
    let seisJ1 = document.getElementById('6Jugador1').value;
    let escJ1 = document.getElementById('escJugador1').value;
    let fullJ1 = document.getElementById('fullJugador1').value;
    let pokerJ1 = document.getElementById('pokerJugador1').value;
    let genJ1 = document.getElementById('genJugador1').value;
    let dgenJ1 = document.getElementById('dgenJugador1').value;
    ttJ1.innerHTML  = parseInt(unoJ1) + parseInt(dosJ1) + parseInt(tresJ1) + parseInt(cuatroJ1) + parseInt(cincoJ1) + parseInt(seisJ1) + parseInt(escJ1) + parseInt(fullJ1) + parseInt(pokerJ1) + parseInt(genJ1) + parseInt(dgenJ1);
    totalJugador1.appendChild(ttJ1);
}

